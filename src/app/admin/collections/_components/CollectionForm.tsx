"use client";

import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Textarea } from "@/components/ui/textarea";
import { formatCurrency } from "@/lib/formatters";
import { useState } from "react";
import { addCollection, updateCollection } from "../../_actions/collections";
import { useFormState, useFormStatus } from "react-dom";
import { Collection } from "@prisma/client";
import Image from "next/image";

export function CollectionForm({ collection }: { collection?: Collection | null }) {
  const [error, action] = useFormState(
    collection == null ? addCollection : updateCollection.bind(null, collection.id),
    {}
  )

  return (
    <form action={action} className="space-y-8">
      <div className="space-y-2">
        <Label htmlFor="name">Name</Label>
        <Input
          type="text"
          id="name"
          name="name"
          required
          defaultValue={collection?.name || ""}
        />
        {error.name && <div className="text-destructive">{error.name}</div>}
      </div>
      <div className="space-y-2">
        <Label htmlFor="description">Description</Label>
        <Textarea
          id="description"
          name="description"
          required
          defaultValue={collection?.description}
        />
        {error.description && (
          <div className="text-destructive">{error.description}</div>
        )}
      </div>
      <div className="space-y-2">
        <Label htmlFor="image">Image</Label>
        <Input type="file" id="image" name="image" required={collection == null} />
        {collection != null && (
          <Image
            src={collection.imagePath}
            height="400"
            width="400"
            alt="Collection Image"
          />
        )}
        {error.image && <div className="text-destructive">{error.image}</div>}
      </div>
      <SubmitButton />
    </form>
  )
}

function SubmitButton() {
  const { pending } = useFormStatus()

  return (
    <Button type="submit" disabled={pending} variant="flat">
      {pending ? "Saving..." : "Save"}
    </Button>
  )
};